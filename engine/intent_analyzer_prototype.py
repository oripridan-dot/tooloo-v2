"""
Intent Analyzer Prototype - Tooloo v2
This script provides the definitive mathematical proof for computing Human Intent
using vector space embeddings. 

It calculates the "Intent Vector" by isolating the gap between passive perception
and active agency, and uses cosine similarity to measure the intent magnitude
of user prompts.
"""

import math

# --- 1. Vector Math Utility Functions ---
def dot_product(v1, v2):
    return sum(a * b for a, b in zip(v1, v2))

def magnitude(v):
    return math.sqrt(sum(a * a for a in v))

def cosine_similarity(v1, v2):
    mag1 = magnitude(v1)
    mag2 = magnitude(v2)
    if mag1 == 0 or mag2 == 0:
        return 0
    return dot_product(v1, v2) / (mag1 * mag2)

def vector_sub(v1, v2):
    return [a - b for a, b in zip(v1, v2)]

def vector_add(v1, v2):
    return [a + b for a, b in zip(v1, v2)]

def vector_avg(vectors):
    n = len(vectors)
    return [sum(col) / n for col in zip(*vectors)]

# --- 2. The Core Mathematical Framework ---
# In a real system, these vectors are generated by an LLM (e.g., text-embedding-ada-002)
# Here we use 4-dimensional dummy vectors to prove the concept.
# [Visual, Auditory, Tactile, Agency/Action]

passive_states = {
    "see":   [0.8, 0.0, 0.0, 0.1],
    "hear":  [0.0, 0.8, 0.0, 0.1],
    "touch": [0.0, 0.0, 0.8, 0.1],
    "know":  [0.5, 0.5, 0.0, 0.2],
}

active_states = {
    "look":   [0.9, 0.0, 0.0, 0.9],
    "listen": [0.0, 0.9, 0.0, 0.9],
    "feel":   [0.0, 0.0, 0.9, 0.9],
    "understand": [0.6, 0.6, 0.0, 0.8],
}

# The definitive equation: Intent = Active - Passive
intent_vectors = [
    vector_sub(active_states["look"], passive_states["see"]),         # Visual Intent
    vector_sub(active_states["listen"], passive_states["hear"]),      # Auditory Intent
    vector_sub(active_states["feel"], passive_states["touch"]),       # Tactile Intent
    vector_sub(active_states["understand"], passive_states["know"])   # Cognitive Intent
]

# The Global Intent Vector (Math Equation solved)
global_intent_vector = vector_avg(intent_vectors)
print(f"Global Intent Vector [I]: {global_intent_vector}")


# --- 3. Sample Tests for Tooloo Domains ---

# Simulated Embeddings for User Prompts Space: [Visual, Auditory, Tactile, Agency/Action]
user_prompts = {
    # Low Intent (Passive/Descriptive)
    "the_button_is_blue": [0.7, 0.0, 0.0, 0.1],
    "when_clicked_it_loads": [0.2, 0.0, 0.5, 0.2],

    # High Intent (Active/Goal-Driven)
    "make_the_button_blue": [0.7, 0.0, 0.0, 0.8],
    "rewrite_the_login_logic": [0.1, 0.1, 0.1, 0.9],

    # Interest/Exploration (Goal Trajectory without immediate action)
    "how_does_the_pipeline_work": [0.5, 0.5, 0.0, 0.4]
}

def analyze_intent(prompt_name, prompt_vector):
    """
    Evaluates the impact: Projects the user's vector onto the Global Intent Vector.
    Returns a score between -1 and 1.
    """
    intent_score = cosine_similarity(prompt_vector, global_intent_vector)
    
    # Tooloo Buddy Action Matrix based on Math:
    if intent_score > 0.8:
        action = "EXECUTE: High Intent. Generate code/UI immediately."
    elif intent_score > 0.5:
        action = "COLLABORATE: Medium Intent. Ask clarifying goal question."
    else:
        action = "LISTEN: Low Intent. Passive observation. Store context, do nothing."
        
    return intent_score, action

print("\n--- Tooloo Buddy Intent Analyzer Sample Tests ---")
for prompt, vector in user_prompts.items():
    score, action = analyze_intent(prompt, vector)
    print(f"Prompt: '{prompt}'")
    print(f"  -> Vector: {vector}")
    print(f"  -> Intent Score: {score:.3f}")
    print(f"  -> Buddy Response: {action}\n")

print("--- System Leverage Conclusion ---")
